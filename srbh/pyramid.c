#include <stdio.h>
int main()
{
    int i, j, n,space,r;

    printf("Enter the value of n: ");
    scanf("%d",&n);
     r=n;

    for(i=0; i<=n; i++)
    {
        for(space=r;space>0;space--)
        {
        printf(" ");
        }
        r--;

        for(j=i; j>=0; j--)
        {
            printf("%d",j);
           
        }
         for(j=1;j<=i;j++)
        {
         printf("%d",j);
         }
        printf("\n");
     }
       return 0;
}
